package Operadores;

public class OperadoresOrdem {
    public static void main (String [] args) {
        /*
        primeiro = parenteses ()
        segundo =  multiplicação e divisão
        terceiro = soma e subtração
        */
        int a = 6 + 2 * 2 ;
        int b = 6 + 2 * (2 - 3);
        System.out.println(a);
        System.out.println(b);
    }
}
