package Operadores;

public class Operadores {
    public static void main (String [] args) {
        int a = 4 + 2;
        int b = 4 - 2;
        int c = 4 * 2;
        double d = (double) 4 / (double) 3;
        int e = 4;
        e++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
